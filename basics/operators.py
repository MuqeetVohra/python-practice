# Arithmetic Operators
num1 = 10
num2 = 5
# addition
res = num1 + num2
print(f'Sum is {res}')
# subtraction
res = num1 - num2
print(f'Subtraction is {res}')
# multiplication
res = num1 * num2
print(f'Product is {res}')
# division
res = num1 / num2
print(f'Division is {res}')
# exponent
res = num1 ** num2
print(f'{num1} exponent {num2} is {res}')
# modulus give the remainder
res = num1 % num2
print(f'Modulus of {num1} and {num2} is {res}')

